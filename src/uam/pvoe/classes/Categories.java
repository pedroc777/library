package uam.pvoe.classes;

/**
 * This is the class Categories, which helps us to creat Categor√≠e type objects. 
 * @author pedro
 */
public class Categories {
    
    /**
     * This attribute is used to obtain the title
     */
    private String titleC;
    
    /**
     * This attribute is used to obtain the name.
     */
    private String nameC;
    
    /**
     * This attribute is used to obtain the last name.
     */
    private String lastNameC;
    
    /**
     * This attribute is used to obtain the key.
     */
    private String keyC;
    
    /**
     * This attribute is used to obtain the number of copies.
     */
    private String numberCo;
    
    /**
     * This attribute is used to obtain the type of the material.
     */
    private String typeC;
    
    /**
     * This attribute is used to obtain the subject.
     */
    private String subjectC;

    public String getTitleC() {
        return titleC;
    }

    public void setTitleC(String titleC) {
        this.titleC = titleC;
    }

    public String getNameC() {
        return nameC;
    }

    public void setNameC(String nameC) {
        this.nameC = nameC;
    }

    public String getLastNameC() {
        return lastNameC;
    }

    public void setLastNameC(String lastNameC) {
        this.lastNameC = lastNameC;
    }

    public String getKeyC() {
        return keyC;
    }

    public void setKeyC(String keyC) {
        this.keyC = keyC;
    }

    public String getNumberCo() {
        return numberCo;
    }

    public void setNumberCo(String numberCo) {
        this.numberCo = numberCo;
    }

    public String getTypeC() {
        return typeC;
    }

    public void setTypeC(String typeC) {
        this.typeC = typeC;
    }

    public String getSubjectC() {
        return subjectC;
    }

    public void setSubjectC(String subjectC) {
        this.subjectC = subjectC;
    }

    
    
    
    
}
